#include "libft.h"

int		ft_numlen(long n)
{
	int	len;

	len = 0;
	if (n == 0)
		len++;
	while (n < 0)
	{
		n = n * -1;
		len++;
	}
	while (n > 0)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
